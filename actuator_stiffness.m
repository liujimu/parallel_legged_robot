% 丝杠轴向力和扭矩的换算
Ps = 32; %导程
F = 1000;
T = F*Ps/2/pi;

%丝杠轴向刚度
E = 2.06e5; %弹性模量，单位MPa
d = 13.5; %丝杠根径，单位mm
l = 50:10:400;
delta_l = 4*F.*l./E./pi./d.^2;
Kl = pi.*E.*d.^2./4./l./10e3;

%丝杠扭转刚度
G = 8.3e4; %剪切弹性模量
theta = 32*T/(pi*d^4*G);
l1 = l + 0.02
delta_T = l1.*theta.*Ps/2/pi;
K_tor = F./delta_T;

%固定端轴承轴向刚度
K_b = 375;

%螺母刚度
K_NS = 80 % 30%
C = 4750
K_ns = K_NS*(F/C/0.3).^(1/3);

%连杆刚度
d1=21;
d2=25;
lt=500;
K_tube = pi*E*(d2^2-d1^2)/4/lt/10e3;

